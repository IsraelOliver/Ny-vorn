    protected override void LoadContent()
    {
        _spriteBatch = new SpriteBatch(GraphicsDevice);
        camera = new Camera2D(GraphicsDevice.Viewport.Width, GraphicsDevice.Viewport.Height);

        tileTexture = Content.Load<Texture2D>("tileMap/orange");

        debugTexture = new Texture2D(GraphicsDevice, 1, 1);
        debugTexture.SetData(new[] { Color.White });

        spriteSheet = Content.Load<Texture2D>("player/playerAnimation");
        player = new Player(spriteSheet);
 
        // TODO: use this.Content to load your game content here
    }

    protected override void Update(GameTime gameTime)
    {
        if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed || Keyboard.GetState().IsKeyDown(Keys.Escape))
            Exit();

        // TODO: Add your update logic here

        Rectangle playerBounds = player.GetBounds();

        // Convertemos a posição do jogador em tiles:
        int minTileX = playerBounds.Left / tileSize;
        int maxTileX = playerBounds.Right / tileSize;
        int minTileY = playerBounds.Top / tileSize;
        int maxTileY = playerBounds.Bottom / tileSize;

        for (int y = minTileY; y <= maxTileY; y++)
        {
            for (int x = minTileX; x <= maxTileX; x++)
            {
                if (IsSolid(x, y))
                {
                    Rectangle tileBounds = new Rectangle(x * tileSize, y * tileSize, tileSize, tileSize);
                    if (playerBounds.Intersects(tileBounds))
                    {
                        Console.WriteLine("Colisão com o tile sólido em: " + x + "," + y);
                        // Aqui você pode bloquear o movimento, ajustar posição, etc.
                    }
                }
            }
        }
        camera.Follow(player.GetPosition());
        player.Update(gameTime);

        base.Update(gameTime);
    }

    protected override void Draw(GameTime gameTime)
    {
        GraphicsDevice.Clear(Color.CornflowerBlue);

        // TODO: Add your drawing code here
        _spriteBatch.Begin(transformMatrix: camera.Transform);
        player.Draw(_spriteBatch);

        for (int y = 0; y < map.GetLength(0); y++)
        {
            for (int x = 0; x < map.GetLength(1); x++)
            {
                if (map[y, x] == 1)
                {
                    _spriteBatch.Draw(tileTexture, new Vector2(x * tileSize, y * tileSize), Color.White);
                }
            }
        }
        _spriteBatch.End();

        base.Draw(gameTime);
    }
